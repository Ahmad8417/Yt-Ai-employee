#!/usr/bin/env python3
"""
Silver Tier Skill for AI Employee
Combines all Silver Tier capabilities in one interface
"""

import os
import sys
import json
import argparse
from pathlib import Path
from datetime import datetime


def get_vault_path() -> Path:
    """Get the vault path"""
    vault = os.getenv('AI_EMPLOYEE_VAULT', './AI_Employee_Vault')
    return Path(vault).resolve()


def check_gmail():
    """Run Gmail watcher to check for new emails"""
    print("\n" + "="*60)
    print("CHECKING GMAIL")
    print("="*60)

    import subprocess
    vault_path = get_vault_path()
    gmail_script = vault_path / 'gmail_watcher.py'

    if not gmail_script.exists():
        print("[FAIL] Gmail watcher not found")
        return False

    result = subprocess.run(
        [sys.executable, str(gmail_script)],
        capture_output=True,
        text=True,
        cwd=str(vault_path.parent)
    )

    print(result.stdout)
    if result.returncode == 0:
        print("\n[OK] Gmail check completed")
        return True
    else:
        print(f"\n[FAIL] Gmail check failed: {result.stderr}")
        return False


def generate_linkedin(category: str = None):
    """Generate LinkedIn post content"""
    print("\n" + "="*60)
    print("GENERATING LINKEDIN POST")
    print("="*60)

    import subprocess
    vault_path = get_vault_path()
    linkedin_script = vault_path / 'linkedin_watcher.py'

    cmd = [sys.executable, str(linkedin_script), 'generate']
    if category:
        cmd.extend(['--category', category])

    result = subprocess.run(
        cmd,
        capture_output=True,
        text=True,
        cwd=str(vault_path.parent)
    )

    print(result.stdout)
    return result.returncode == 0


def queue_linkedin(category: str = None):
    """Queue a LinkedIn post for approval"""
    print("\n" + "="*60)
    print("QUEUING LINKEDIN POST")
