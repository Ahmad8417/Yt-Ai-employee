def create_plan(task_id: str = None):
    """Create a plan for a task"""
    print("\n" + "="*60)
    print("CREATING PLAN")
    print("="*60)

    vault_path = get_vault_path()
    needs_action = vault_path / 'Needs_Action'

    if task_id:
        # Find specific task
        task_files = list(needs_action.glob(f"*{task_id}*.md"))
    else:
        # Get most recent task
        task_files = sorted(needs_action.glob("*.md"), key=lambda p: p.stat().st_mtime, reverse=True)

    if not task_files:
        print("[FAIL] No tasks found in Needs_Action")
        return False

    task_file = task_files[0]
    print(f"Creating plan for: {task_file.name}")

    # Read task content
    content = task_file.read_text(encoding='utf-8')

    # Create plan
    plan_folder = vault_path / 'Plans'
    plan_folder.mkdir(parents=True, exist_ok=True)

    timestamp = datetime.now().strftime('%Y%m%d_%H%M%S')
    plan_file = plan_folder / f"PLAN_{task_file.stem}_{timestamp}.md"

    plan_content = f"""---
type: plan
source_task: {task_file.name}
created: {datetime.now().isoformat()}
status: pending
---

# Plan for Task: {task_file.stem}

## Task Description
```
{content[:500]}...
```

## Proposed Steps
1. [ ] Analyze the task requirements
2. [ ] Gather necessary information
3. [ ] Execute the primary action
4. [ ] Verify results
5. [ ] Complete and move to Done

## Resources Needed
- [ ] Review task content
- [ ] Check relevant documentation
- [ ] Execute action

## Estimated Time
- Analysis: 5 minutes
- Execution: 15 minutes
- Verification: 5 minutes
- Total: 25 minutes

## Notes
_Add any specific notes about this plan_

---

*Plan generated by AI Employee - Silver Tier*
"""

    plan_file.write_text(plan_content, encoding='utf-8')
    print(f"[OK] Plan created: {plan_file.name}")

    return True


def create_approval(approval_type: str, details: dict):
    """Create an approval request"""
    print("\n" + "="*60)
    print("CREATING APPROVAL REQUEST")
    print("="*60)

    import subprocess

    cmd = [
        sys.executable,
        '.claude/skills/approval-workflow.py',
        'create-request',
        '--action-type', approval_type,
        '--details', json.dumps(details)
    ]

    result = subprocess.run(
        cmd,
        capture_output=True,
        text=True,
        cwd=str(get_vault_path().parent)
    )

    print(result.stdout)
    return result.returncode == 0


