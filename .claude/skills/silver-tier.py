def run_scheduled():
    """Run scheduled tasks"""
    print("\n" + "="*60)
    print("RUNNING SCHEDULED TASKS")
    print("="*60)

    import subprocess
    vault_path = get_vault_path()
    scheduler_script = vault_path / 'scheduler.py'

    result = subprocess.run(
        [sys.executable, str(scheduler_script), 'list'],
        capture_output=True,
        text=True,
        cwd=str(vault_path.parent)
    )

    print(result.stdout)
    return result.returncode == 0


def main():
    """Main entry point"""
    parser = argparse.ArgumentParser(description='Silver Tier Skill for AI Employee')
    parser.add_argument('action', choices=[
        'check-gmail', 'generate-linkedin', 'queue-linkedin',
        'create-plan', 'create-approval', 'check-approved', 'run-scheduled'
    ], help='Action to perform')
    parser.add_argument('--task-id', help='Task ID for plan creation')
    parser.add_argument('--category', help='LinkedIn category')
    parser.add_argument('--approval-type', help='Approval request type')
    parser.add_argument('--details', help='Approval details (JSON)')

    args = parser.parse_args()

    if args.action == 'check-gmail':
        check_gmail()

    elif args.action == 'generate-linkedin':
        generate_linkedin(args.category)

    elif args.action == 'queue-linkedin':
        queue_linkedin(args.category)

    elif args.action == 'create-plan':
        create_plan(args.task_id)

    elif args.action == 'create-approval':
        if not args.approval_type:
            print("Error: --approval-type required")
            sys.exit(1)
        details = json.loads(args.details) if args.details else {}
        create_approval(args.approval_type, details)

    elif args.action == 'check-approved':
        check_approved()

    elif args.action == 'run-scheduled':
        run_scheduled()


if __name__ == '__main__':
    main()
